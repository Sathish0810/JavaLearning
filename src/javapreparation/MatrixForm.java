public class MatrixForm {

	public static void main(String[] args) {
    for(int i=0;i<=4;i++)
    {
    	for(int j=0;j<=4;j++)
    	{
    		for (int k=0;k<=4;k++)
    				{
    			System.out.print(i+""+j+""+k+ " ");
    				}
        	System.out.println();

    	}
    	
    }
    byte b1=10;
    byte b2=10;
    byte b3=(byte)(b1+b2);
    System.out.println(b3);
    
	}

}
